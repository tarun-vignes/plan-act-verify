from __future__ import annotations

from http.server import BaseHTTPRequestHandler, HTTPServer
import json

from .api import ApiRouter


class RequestHandler(BaseHTTPRequestHandler):
    router = ApiRouter()

    def _send(self, status_code: int, body: dict[str, object]) -> None:
        encoded = json.dumps(body).encode("utf-8")
        self.send_response(status_code)
        self.send_header("Content-Type", "application/json")
        self.send_header("Content-Length", str(len(encoded)))
        self.end_headers()
        self.wfile.write(encoded)

    def do_GET(self) -> None:  # noqa: N802 - BaseHTTPRequestHandler contract
        response = self.router.handle("GET", self.path)
        self._send(response.status_code, response.body)

    def do_POST(self) -> None:  # noqa: N802 - BaseHTTPRequestHandler contract
        length = int(self.headers.get("Content-Length", "0"))
        raw = self.rfile.read(length) if length else b"{}"
        payload = json.loads(raw.decode("utf-8"))
        response = self.router.handle("POST", self.path, payload)
        self._send(response.status_code, response.body)

    def log_message(self, format: str, *args: object) -> None:
        # The generated service is quiet by default so CI logs stay readable.
        return


def serve(host: str = "127.0.0.1", port: int = 8000) -> None:
    HTTPServer((host, port), RequestHandler).serve_forever()


