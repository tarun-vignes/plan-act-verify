class PrototypeService:
    """Coordinates validation and persistence around the generated schema."""

    def __init__(self, repository: InMemoryRepository | None = None) -> None:
        self.repository = repository or InMemoryRepository()
        self.fields = PRODUCT_CONFIG["field_definitions"]

    def _validate(self, payload: dict[str, object]) -> dict[str, object]:
        validated: dict[str, object] = {{}}
        for field in self.fields:
            name = field["name"]
            field_type = field["field_type"]
            required = field["required"]
            default = field["default"]
            if name not in payload or payload[name] in (None, ""):
                if default is not None:
                    validated[name] = default
                    continue
                if required:
                    raise ValidationError(f"Missing required field: {{name}}")
                continue
            value = payload[name]
            if field_type == "int" and not isinstance(value, int):
                raise ValidationError(f"Field {{name}} must be an integer")
            if field_type == "str" and not isinstance(value, str):
                raise ValidationError(f"Field {{name}} must be a string")
            validated[name] = value
        return validated

    def create_record(self, payload: dict[str, object]) -> Record:
        validated = self._validate(payload)
