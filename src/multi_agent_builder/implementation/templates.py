from __future__ import annotations

from dataclasses import asdict
from typing import Any

from .config import PRODUCT_CONFIG
from .models import Response, ValidationError
from .service import PrototypeService


class ApiRouter:
    def __init__(self, service: PrototypeService | None = None) -> None:
        self.service = service or PrototypeService()

    def handle(self, method: str, path: str, payload: dict[str, Any] | None = None) -> Response:
        collection_path = f"/{PRODUCT_CONFIG['entity_collection_path']}"
        if method == "GET" and path == "/health":
            return Response(200, {"status": "ok", "product": PRODUCT_CONFIG["product_name"]})
        if method == "GET" and path == "/metadata":
            return Response(
                200,
                {
                    "product_name": PRODUCT_CONFIG["product_name"],
                    "entity_collection": PRODUCT_CONFIG["entity_collection_path"],
                    "fields": PRODUCT_CONFIG["field_definitions"],
                    "capabilities": PRODUCT_CONFIG["capabilities"],
                },
            )
        if method == "GET" and path == collection_path:
            records = [asdict(record) for record in self.service.list_records()]
            return Response(200, {"items": records, "count": len(records)})
        if method == "POST" and path == collection_path:
            try:
                record = self.service.create_record(payload or {})
            except ValidationError as exc:
                return Response(400, {"error": str(exc)})
            return Response(201, {"id": record.id, "record": asdict(record)})
        if method == "GET" and path.startswith(f"{collection_path}/"):
            record_id = path.split("/")[-1]
            record = self.service.get_record(record_id)
            if record is None:
                return Response(404, {"error": "Not found"})
            return Response(200, {"item": asdict(record)})
        return Response(404, {"error": "Route not found"})
