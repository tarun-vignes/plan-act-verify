def _config_json(context: PrototypeContext) -> str:
    payload = {
        "product_name": context.product_name,
        "product_slug": context.product_slug,
        "entity_label_singular": context.entity_label_singular,
        "entity_label_plural": context.entity_label_plural,
        "entity_collection_path": context.entity_collection_path,
        "field_definitions": [
            {
                "name": field.name,
                "field_type": field.field_type,
                "description": field.description,
                "required": field.required,
                "default": field.default,
            }
            for field in context.field_definitions
        ],
        "capabilities": context.capabilities,
        "constraints": context.constraints,
    }
    return json.dumps(payload, indent=4)


