def render_files(
    requirements: RequirementArtifact,
    architecture: ArchitectureArtifact,
) -> dict[str, str]:
    rendered: dict[str, str] = {}
    for partial in (
        render_surface_files(requirements, architecture),
        render_domain_files(requirements),
        render_delivery_files(requirements),
    ):
        rendered.update(partial)
    return rendered
